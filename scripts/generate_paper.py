add_heading_styled('4.2  模块一：声学校准适配器', level=2)

add_para('【要解决的问题】', bold=True, first_indent=True)
add_para(
    '第3.2节的分析表明，WavLM的特征空间对儿童语音存在偏移，且这种偏移在不同帧（元音帧、辅音帧、静音帧等）'
    '之间不是均匀的。如果直接使用有偏的特征进行分类，效果会打折扣。我们需要一个轻量的机制来"纠正"这种偏移。'
)

add_para('【设计思路】', bold=True, first_indent=True)
add_para(
    '声学校准适配器的核心设计是一个两阶段的校准结构：'
)
add_para(
    '第一阶段（基础校准）：对每一帧的768维特征，逐维度乘以一个可学习的缩放系数（scale），再加上一个可学习的'
    '偏移量（bias）。这类似于对每个特征维度进行"微调"——scale>1表示增强该维度，scale<1表示抑制该维度。'
    'scale和bias各768个参数，合计1,536个参数。公式为：校准后特征 = 原始特征 × scale + bias。'
)
add_para(
    '第二阶段（自适应微调）：用一个轻量级的门控网络（gate network，结构为768→192→768，'
    '约30万参数）来分析每帧的内容，输出一个调节信号。如果某帧的特征看起来"更像成人"，调节信号就更强；'
    '反之就更弱。该信号以很小的幅度（乘以0.1）叠加到第一阶段的结果上，实现帧级自适应校准。'
    '完整公式为：校准后特征 = x × scale + bias + 0.1 × gate(x) × x，其中x是原始特征，'
    'scale和bias是可学习参数，gate是一个小型神经网络。'
)
add_para(
    '两阶段的设计使得适配器既有全局的校准能力（第一阶段），又能根据每帧的具体内容灵活调整（第二阶段），'
    '同时保持极低的额外参数量（约30万，仅占WavLM模型的0.3%），可以在显存有限的设备上高效运行。'
)

add_para('【两种初始化方式】', bold=True, first_indent=True)
add_para(
    '适配器支持两种参数初始化方式：（1）统计先验初始化——利用第3.2节在儿童和成人数据上分别计算的'
    '均值和标准差，直接算出scale和bias的初始值（scale=成人标准差/儿童标准差，bias=成人均值-儿童均值×scale）。'
    '（2）随机初始化——scale初始化为全1向量，bias初始化为全0向量，由训练过程自动学习。'
    '消融实验（见5.3节）将对比两种方式的效果，以判断统计先验是否带来额外收益。'
)

add_heading_styled('4.3  模块二：时序重要性池化', level=2)

add_para('【要解决的问题】', bold=True, first_indent=True)
add_para(
    '第3.1节的分析表明，儿童的语速和音高变化很不均匀。在同一条语音中，情绪信息并不是平均分布在每一帧上的——'
    '有的帧是情绪表达的关键时刻（如突然提高音量），有的帧则信息量较少（如停顿或过渡段）。'
    '简单的均值池化（对所有帧平等地取平均）会把这些关键的时间线索抹平掉，丢失重要的情绪信息。'
)

add_para('【设计思路】', bold=True, first_indent=True)
add_para(
    '时序重要性池化的基本思路是：不是所有帧都同等重要，让模型学会"关注"那些更可能包含情绪信息的帧。'
    '具体实现分为三步：'
)
add_para(
    '第一步（提取韵律特征）：对每帧语音，使用信号处理工具（librosa库的YIN算法）提取基频（F0，反映音高）'
    '和RMS能量（反映响度）两个韵律参数。基频检测范围为65~2093 Hz，覆盖儿童的高频范围。'
    '这两个参数归一化到[0, 1]区间。'
)
add_para(
    '第二步（生成注意力权重）：将每帧的WavLM特征（768维）和韵律参数（2维）拼接为770维（实际实现中先将韵律参数'
    '投影到64维再与特征拼接，拼接后为832维），送入一个小型网络（832→128→1），为每帧输出一个分数。'
    '分数越高，表示该帧越值得关注。所有帧的分数通过softmax归一化为概率权重。'
)
add_para(
    '第三步（加权求和）：用各帧的权重对WavLM特征进行加权求和，得到一条768维的全局特征向量。'
    '关键帧（如音高剧烈变化、能量突然上升的帧）获得高权重，过渡帧获得低权重。'
)
add_para(
    '与纯数据驱动的自注意力池化（仅用WavLM特征自身计算权重，不引入韵律信息）不同，本模块显式地利用了'
    '基频和能量这两个已被大量研究证实与情绪表达密切相关的物理量来引导注意力，使得池化过程更具可解释性——'
    '我们可以直观地看到哪些帧被模型认为"重要"，以及这些帧对应了什么样的韵律特征。'
)

add_heading_styled('4.4  模块三：分布约束增强', level=2)

add_para('【要解决的问题】', bold=True, first_indent=True)
add_para(
    '数据增强（如对语音进行音高偏移或语速拉伸）是提升深度学习模型泛化能力的常用手段。然而，'
    '成人的增强参数范围（如pitch shift ±6半音，语速拉伸0.7~1.3倍）直接用于儿童语音时，'
    '可能制造出"不像儿童"的合成语音——例如把本来就高的儿童音调再偏移±6半音，可能超出儿童真实的音域范围。'
    '这种偏离真实分布的合成数据不仅无助于训练，反而可能误导模型。'
)

add_para('【设计思路】', bold=True, first_indent=True)
add_para(
    '分布约束增强的基本逻辑是：增强参数的范围应该由儿童语音自身的统计分布来决定，而非照搬成人参数。'
    '具体做法是：（1）从儿童语音数据中统计F0的分布范围，据此确定pitch shift的合理上限；'
    '（2）统计语速的分布范围，据此确定time stretch的合理上下界。基于第3.1节的统计结果，'
    '儿童的音高偏移不宜超过±3半音，语速拉伸应限制在0.85~1.15倍之间。'
)

add_para('【负面实验设计】', bold=True, first_indent=True)
add_para(
    '为了严格验证分布约束的必要性，我们设计了一组对比实验（而非单一实验）。'
    '四个实验配置使用完全相同的模型（WavLM + Adapter + Prosody Pooling + DrseCNN），'
    '唯一的变化是训练数据的增强参数不同：'
)
add_para(
    'C1（无增强）：不使用任何增强，作为参照基线。'
)
add_para(
    'C3（儿童约束增强）：pitch shift限制在±3半音、time stretch限制在0.85~1.15倍，'
    '参数范围由儿童统计分布确定。'
)
add_para(
    'C2（成人参数增强）：pitch shift ±6半音、time stretch 0.7~1.3倍，模拟成人SER中的常见设置。'
)
add_para(
    'C4（极端参数增强）：pitch shift ±12半音、time stretch 0.5~1.5倍，模拟无约束的极端情况。'
)
add_para(
    '对于C2、C3、C4，我们分别计算其增强后特征与C1（无增强）特征之间的分布距离（FD），'
    '用于定量衡量增强导致的分布偏移程度。核心假设是：FD越大（偏离越远），分类准确率越低。'
    '实验结论见第5.4节。'
)

